// NextAuth.js configuration for SparkyPass
import NextAuth from "next-auth";
import Google from "next-auth/providers/google";
import Facebook from "next-auth/providers/facebook";
import Apple from "next-auth/providers/apple";
import Credentials from "next-auth/providers/credentials";
import { compare } from "bcryptjs";
import { db, users, verificationTokens } from "@/lib/db";
import { eq, and, gt } from "drizzle-orm";

export const { handlers, auth, signIn, signOut } = NextAuth({
  providers: [
    Google({
      clientId: process.env.GOOGLE_CLIENT_ID!,
      clientSecret: process.env.GOOGLE_CLIENT_SECRET!,
    }),
    Facebook({
      clientId: process.env.FACEBOOK_CLIENT_ID!,
      clientSecret: process.env.FACEBOOK_CLIENT_SECRET!,
    }),
    Apple({
      clientId: process.env.APPLE_CLIENT_ID!,
      clientSecret: process.env.APPLE_CLIENT_SECRET!,
    }),
    Credentials({
      name: "credentials",
      credentials: {
        email: { label: "Email", type: "email" },
        password: { label: "Password", type: "password" },
        verificationToken: { label: "Verification Token", type: "text" },
      },
      async authorize(credentials) {
        // Auto-login after email verification (one-time token)
        const verificationToken = credentials?.verificationToken as string | undefined;
        if (verificationToken) {
          const [tokenRecord] = await db
            .select()
            .from(verificationTokens)
            .where(
              and(
                eq(verificationTokens.token, verificationToken),
                gt(verificationTokens.expiresAt, new Date())
              )
            )
            .limit(1);

          if (!tokenRecord) return null;

          const [user] = await db
            .select()
            .from(users)
            .where(eq(users.id, tokenRecord.userId))
            .limit(1);

          // Delete the token (one-time use)
          await db
            .delete(verificationTokens)
            .where(eq(verificationTokens.id, tokenRecord.id));

          if (!user || !user.emailVerified) return null;

          return { id: user.id, email: user.email, name: user.name };
        }

        // Standard email + password login
        if (!credentials?.email || !credentials?.password) {
          return null;
        }

        const email = credentials.email as string;
        const password = credentials.password as string;

        // Find user by email
        const [user] = await db
          .select()
          .from(users)
          .where(eq(users.email, email))
          .limit(1);

        if (!user || !user.passwordHash) {
          return null;
        }

        // Verify password
        const isValidPassword = await compare(password, user.passwordHash);
        if (!isValidPassword) {
          return null;
        }

        return {
          id: user.id,
          email: user.email,
          name: user.name,
        };
      },
    }),
  ],
  pages: {
    signIn: "/login",
    newUser: "/register/profile",
    error: "/login",
  },
  session: {
    strategy: "jwt",
  },
  callbacks: {
    async signIn({ user, account }) {
      if (account?.provider !== "credentials") {
        // Handle OAuth sign-in
        const email = user.email;
        if (!email) return false;

        // Check if user exists
        const [existingUser] = await db
          .select()
          .from(users)
          .where(eq(users.email, email))
          .limit(1);

        if (!existingUser) {
          // Create new user for OAuth
          const newUserId = crypto.randomUUID();
          await db.insert(users).values({
            id: newUserId,
            email: email,
            name: user.name || "User",
            authProvider: account?.provider as "google" | "facebook" | "apple",
            emailVerified: true, // OAuth users are considered verified
            trialEndsAt: new Date(Date.now() + 7 * 24 * 60 * 60 * 1000),
            subscriptionStatus: "trialing",
          });
          user.id = newUserId;
        } else {
          user.id = existingUser.id;
        }
      }
      return true;
    },
    async jwt({ token, user }) {
      if (user) {
        token.id = user.id;
      }

      // Fetch fresh user data for profile completion status
      if (token.id) {
        const [dbUser] = await db
          .select()
          .from(users)
          .where(eq(users.id, token.id as string))
          .limit(1);

        if (dbUser) {
          token.profileComplete = Boolean(
            dbUser.username && dbUser.city && dbUser.state && dbUser.dateOfBirth && dbUser.targetExamDate
          );
          token.isEmailVerified = dbUser.emailVerified;
          token.subscriptionStatus = (dbUser.subscriptionStatus as "trialing" | "active" | "past_due" | "canceled" | "expired") ?? null;
          token.trialEndsAt = dbUser.trialEndsAt?.toISOString() ?? null;
          token.subscriptionPeriodEnd = dbUser.subscriptionPeriodEnd?.toISOString() ?? null;
        }
      }

      return token;
    },
    async session({ session, token }) {
      return {
        ...session,
        user: {
          ...session.user,
          id: token.id as string,
          profileComplete: (token.profileComplete as boolean) ?? false,
          isEmailVerified: (token.isEmailVerified as boolean) ?? false,
          subscriptionStatus: token.subscriptionStatus ?? null,
          trialEndsAt: token.trialEndsAt ?? null,
          subscriptionPeriodEnd: token.subscriptionPeriodEnd ?? null,
        },
      };
    },
  },
});
